    private void llenarTabla() {
        // Conexi√≥n a la base de datos y consulta

        
        try (Connection con = DriverManager.getConnection(url, user, password)) {
            String query = "SELECT voto, COUNT(*) as total_votos FROM votos GROUP BY voto";
            PreparedStatement pst = con.prepareStatement(query);
            ResultSet rs = pst.executeQuery();

            // Crea un modelo de tabla para almacenar los resultados
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Candidato");
            model.addColumn("Total de Votos");

            while (rs.next()) {
                String candidato = rs.getString("voto");
                int totalVotos = rs.getInt("total_votos");
                model.addRow(new Object[]{candidato, totalVotos});
            }

            // Asigna el modelo de la tabla
            tabla.setModel(model);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

